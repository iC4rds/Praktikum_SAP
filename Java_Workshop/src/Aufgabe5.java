package Aufgaben;

public class Aufgabe5 {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}
		int j = 0;
		while (j < 10) {
			j++;
			System.out.print(j + " ");
		}
		
		int l = 0;
		do {
			l++;
			System.out.print(l + " ");
		} while (l < 10);
	}

}
